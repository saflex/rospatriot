        }
        else if ($.getQuery('theme') === 'image') {
            $(function () {
                $.scrollUp({
                    animation: 'fade',
                    activeOverlay: '#00FFFF',
                    scrollImg: {active: true, type: 'background', src: 'img/top.png'}
                });
            });
